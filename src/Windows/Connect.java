    private Integer convPort(String Port) {
        Integer IPPort = 0;
        if (Port.equals("34161")) {
            IPPort = Integer.parseInt("34080");
        } else if (Port.equals("161")) {
            IPPort = Integer.parseInt("80");
        }
        return IPPort;
    }


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String s = "";
        int i = jComboBox1.getSelectedIndex();
        boolean b = MainItc.ITC.comData.testLAN(Configuration.Config.Stations[i][1], convPort(Configuration.Config.Stations[i][2])); //test dzia≈Ça tylko dla interfejs√≥w (snmp go nie ma)
        if (b) {
            jLabel5.setForeground(Color.white);
            jLabel5.setBackground(new Color(0, 150, 0));
            s = "Address " + Configuration.Config.Stations[i][1] + " reachable...";
            jLabel5.setText(s);
            LOG.setLog(1, "Manual test connection: <" + s + ">");
        } else {
            jLabel5.setForeground(Color.yellow);
            jLabel5.setBackground(Color.red);
            s = "Address " + Configuration.Config.Stations[i][1] + " unreachable!!!";
            jLabel5.setText(s);
            LOG.setLog(2, "Manual test connection: <" + s + ">");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

