    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = jComboBox1.getSelectedIndex();
        if (i >= 0) {
            MainItc.ITC.mainWin.viewStation.setText("    Station: " + Configuration.Config.Stations[i][0] + "    IP: " + Configuration.Config.Stations[i][1] + "    User: " + Configuration.Config.Stations[i][3]);
            LOG.setLog(1, "Selected: <Station: " + Configuration.Config.Stations[i][0] + "; IP: " + Configuration.Config.Stations[i][1] + ":" + Configuration.Config.Stations[i][2] + "; User: " + Configuration.Config.Stations[i][3] + ">");
            Configuration.Config.indexConnection = i;
            MainItc.ITC.mainWin.doShow();
            MainItc.ITC.comData.SettingDME(Configuration.Config.Stations[i][1], Configuration.Config.Stations[i][2], Configuration.Config.Stations[i][3], Configuration.Config.Stations[i][4]);
            MainItc.ITC.webData.SettingLog(Configuration.Config.Stations[i][1], Configuration.Config.Stations[i][2], Configuration.Config.Stations[i][3], Configuration.Config.Stations[i][4], Configuration.Config.Stations[i][5]);
            this.dispose();
            ITC.webData.webThred();
            ComData.collectDataFromDME();

        } else {
            LOG.setLog(2, "Wrong station selection...");
            JOptionPane.showMessageDialog(this, "Before connection You have to choose a station.", "Warning", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

